package isp.scala.practice

object BasicDataTypes {

  /**
    * @return true Boolean value
    */
  def defineBoolean(): Boolean = {
    true
  }

  /**
    * @return Byte value of -16
    */
  def defineByte(): Byte = {
    -16 toByte
  }

  /**
    * @return Int value of 42
    */
  def defineInteger(): Int = {
    42
  }

  /**
    * @return Long value of 1 500 250
    */
  def defineLong(): Long = {
    1500250L
  }

  /**
    * @return Float value of 1.5
    */
  def defineFloat(): Float = {
    1.5F
  }

  /**
    * @return Double value of 1.0
    */
  def defineDouble(): Double = {
    1.0
  }

}
